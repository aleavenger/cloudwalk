from __future__ import annotations

import csv
import textwrap
from dataclasses import dataclass
from pathlib import Path


@dataclass(frozen=True)
class CheckoutHour:
    time: str
    today: float
    baseline: float
    absolute_deviation: float
    relative_deviation: float
    is_material: bool
    direction: str
    zero_gap: bool
    severity_score: float


@dataclass(frozen=True)
class FocusCluster:
    direction: str
    start_idx: int
    end_idx: int
    score: float
    peak_idx: int
    has_zero_gap: bool


@dataclass(frozen=True)
class Verdict:
    label: str
    level: str
    summary: str
    investigate_first: bool


